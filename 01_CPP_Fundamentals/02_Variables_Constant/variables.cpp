#include <iostream>

int main()
{
    // Syntax for declaring a variable: data_type identifier;
    int age;

    // Syntax for defining a variable: data_type identifier = value;
    double height = 5.9;

    // Assigning value to already declared variable
    age = 18;

    // Reassigning a value to a variable
    height = 6.0;

    // Printing variable
    std::cout << "AGE = " << age << "\nHEIGHT = " << height << '\n';

    return 0;
}